    private void botGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botGuardarActionPerformed
        Double uprecio = 0.0;
        Accion acc = new Accion();
        acc.setNombre(txtNombre.getText().toString());
        acc.setSimbolo(txtSimbolo.getText().toString());
        acc.setTipo(txtTipo.getText().toString());
        uprecio = Double.parseDouble(txtUltPrecio.getText().toString());
        acc.setUltimoPrecio(uprecio);

        //modificar tabla
        editarTabla editTab = new editarTabla();
        editTab.editarAccion(acc, accPK);
        
        
        JOptionPane.showMessageDialog(null, "Accion editada");
        dispose();
        ini.cargarVentana();
       
    }//GEN-LAST:event_botGuardarActionPerformed

